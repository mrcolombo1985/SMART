    public EditSensorDialog(java.awt.Frame parent, boolean modal, String sensor) {
        super(parent, modal);
        initComponents();
        try {
            arduinoName.removeAll();
            ArduinoDeviceProvider arduinoDeviceProvider = new ArduinoDeviceProvider();
            arduinoDeviceProvider.fromServer().forEach(x->{
            arduinoName.addItem(x.getName());
            });
        } catch (URISyntaxException ex) {
            Logger.getLogger(EditSensorDialog.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(EditSensorDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            
            SensorProvider sensorProvider = new SensorProvider();
            SensorDTO sensorDTO = sensorProvider.fromServerByName(sensor);
            this.addressSensor.setText(sensorDTO.getAddress());
            this.arduinoName.setSelectedItem(sensorDTO.getArduino());
            this.pinSensorGroup.setText(sensorDTO.getPin());
            this.sensorName.setText(sensorDTO.getName());
            this.sensorDescription.setText(sensorDTO.getDescription());
        } catch (URISyntaxException ex) {
            Logger.getLogger(EditSensorDialog.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(EditSensorDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
    }

