    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        sensorNewDTO = SensorDTO
                            .builder()
                                    .name("")
                                    .arduino(arduinoName.getSelectedItem().toString())
                                    .pin(this.pinSensorGroup.getText())
                                    .description(this.sensorDescription.getText())
                            .build();
            String sensorName = sensorOldDTO.getName();
            sensorOldDTO.updatedata(sensorNewDTO);
            SensorProvider sensorProvider = new SensorProvider();
            sensorProvider.update(sensorName, sensorOldDTO);
            this.setVisible(false);
    }//GEN-LAST:event_jButton2ActionPerformed

